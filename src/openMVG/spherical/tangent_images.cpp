/* Constructor */
TangentImages::TangentImages(const int base_level, const int sphere_level,
                             const int rect_h, const int rect_w)
    : base_level(base_level),
      sphere_level(sphere_level),
      rect_h(rect_h),
      rect_w(rect_w) {
  // Ensure that the base level is not outside the supported range
  if (this->base_level < 0 || this->base_level > 2) {
    throw std::invalid_argument("Base level must be in {0, 1, 2}");
  }

  // Compute the number and dim
  this->ComputeNum();
  this->ComputeDim();

  // Compute the corners of the tangent images. This will be used when
  // converting coordinates from tangent images to equirectangular.
  this->ComputeTangentImageCorners();
}

/*
  Compute the corners of the tangent images in 3D coordinates for use
  converting UV coordinates on a tangent plane to XY coordinates in the
  equirectangular image, storing the result in the class variable <corners>
 */
void TangentImages::ComputeTangentImageCorners() {
  // Get the centers of the tangent images in spherical coordinates
  std::vector<double> centers;
  this->GetTangentImageCenters(centers);

  // Get the tangent image info
  const double ang_res = this->GetVertexAngularResolution();
  const double tangent_dim = static_cast<double>(this->dim);

  // Compute the corners via the gnomonic projection
  const double d = ang_res * static_cast<double>(this->dim - 1) /
                   static_cast<double>(this->dim);
  std::vector<double> spherical_corners;
  this->InverseGnomonicKernel(centers, 2, 2, d, d, spherical_corners);

  this->corners.resize(this->num * 8);
  for (size_t i = 0; i < this->num * 8; i++)
    this->corners[i] = ConvertSphericalTo3D(
        Vec2(spherical_corners[2 * i], spherical_corners[2 * i + 1]));
}

void TangentImages::InverseGnomonicKernel(
    const std::vector<double> lonlat_in, const int kh, const int kw,
    const double res_lon, const double res_lat,
    std::vector<double> &lonlat_out) const {
  // Allocate memory for output
  lonlat_out.resize(lonlat_in.size() * kh * kw);

#pragma omp parallel for
  // Go through each tangent image
  for (size_t i = 0; i < lonlat_in.size() / 2; i++) {
    // Tangent point for each tangent image in spherical coordinates
    const double lon = lonlat_in[2 * i];
    const double lat = lonlat_in[2 * i + 1];

    // j indexes rows in the tangent image
    for (size_t j = 0; j < kh; j++) {
      double y =
          (static_cast<double>(j) - static_cast<double>(kh) / 2.0) * res_lat;
      if (kh % 2 == 0) {
        y += res_lat / 2.0;
      }
      // k indexes columns in the tangent image
      for (size_t k = 0; k < kw; k++) {
        double x =
            (static_cast<double>(k) - static_cast<double>(kw) / 2.0) * res_lon;
        if (kw % 2 == 0) {
          x += res_lon / 2.0;
        }

        // Index in output vector
        const size_t vec_idx = i * kh * kw * 2 + j * kw * 2 + k * 2;

        // Compute the inverse gnomonic projection of each (x,y) on a plane
        // centered at (lon, lat) [output is in spherical coordinates (radians)]
        const double rho = std::sqrt(x * x + y * y);
        const double nu = std::atan(rho);

        // Compute output longitude (modulo 2*PI)
        lonlat_out[vec_idx] =
            NegMod(lon +
                       std::atan2(x * std::sin(nu),
                                  rho * std::cos(lat) * std::cos(nu) -
                                      y * std::sin(lat) * std::sin(nu)) +
                       M_PI,
                   2 * M_PI) -
            M_PI;

        // Compute output latitude
        lonlat_out[vec_idx + 1] =
            std::asin(std::cos(nu) * std::sin(lat) +
                      y * std::sin(nu) * std::cos(lat) / rho);
      }
    }
  }
}

/*
  Creates a resampling map to create tangent images
*/
void TangentImages::CreateEquirectangularToTangentImagesSampleMap(
    std::vector<double> &sampling_map) const {
  // Allocate space for all the tangent image sample maps
  sampling_map.resize(this->num * this->dim * this->dim * 2);

  // Sampling resolution of the tangent images
  const double sample_resolution =
      this->GetVertexAngularResolution() / this->dim;

  // Tangent image locations
  std::vector<double> tangent_centers;
  this->GetTangentImageCenters(tangent_centers);
  this->InverseGnomonicKernel(tangent_centers, this->dim, this->dim,
                              sample_resolution, sample_resolution,
                              sampling_map);

#pragma omp parallel for
  // Go through each tangent image
  for (size_t i = 0; i < this->num; i++) {
    // j indexes rows in the tangent image
    for (size_t j = 0; j < this->dim; j++) {
      // k indexes columns in the tangent image
      for (size_t k = 0; k < this->dim; k++) {
        // Index in sample map
        const size_t map_idx =
            i * this->dim * this->dim * 2 + j * this->dim * 2 + k * 2;

        // Convert the output to coordinates of an equirectangular image
        sampling_map[map_idx] = Rescale(sampling_map[map_idx], -M_PI, M_PI, 0.0,
                                        static_cast<double>(rect_w - 1));
        sampling_map[map_idx + 1] =
            Rescale(sampling_map[map_idx + 1], -M_PI / 2.0, M_PI / 2.0, 0.0,
                    static_cast<double>(rect_h - 1));
      }
    }
  }
}

/*
  Number of tangent images is 20(4^b), where b is the base level
*/
void TangentImages::ComputeNum() {
  this->num = 20 * (1 << (2 * this->base_level));
}

/*
  Tangent image dimension is 2 ^ (s-b), where s is sphere level and b is
  base level. Note, tangent images are square.
*/
void TangentImages::ComputeDim() {
  this->dim = 1 << (this->sphere_level - this->base_level);
}

/*
  Computes the field of view of these tangent images as an
  average over all of them as there is slight variation due to the singular
  points of the icosahedron after subdivision
*/
const double TangentImages::FOV() const {
  // Accumulator variable for mean computation
  double accum = 0.0;

  // Go through each set of N tangent image corners (N x 4 in memory)
  for (size_t i = 0; i < this->num; i++) {
    // Get the rays to 2 corners
    Vec3 tl = this->corners[4 * i];
    Vec3 tr = this->corners[4 * i + 1];

    // Normalize the rays to vectors from the origin
    tl.normalize();
    tr.normalize();

    // Compute the angle between the vectors to get the FOV. Because tangent
    // images are square, we only do this in one direction
    accum += std::acos(tl.dot(tr));
  }

  // Computer average in radians
  accum /= this->num;

  // Return FOV in degrees
  return 180 * accum / M_PI;
}

const bool TangentImages::RayIntersectsTriangle(const Vec3 &pt, const Vec3 &A,
                                                const Vec3 &B,
                                                const Vec3 &C) const {
  // Compute barycentric coordinates
  // s --> A
  // t --> B
  // u --> C
  // Compute the necessary vectors
  const auto BA = B - A;
  const auto CA = C - A;
  const auto W = pt - A;

  // Area of the parallelogram
  const auto N = BA.cross(CA);

  // Compute the ratios of subtriangles
  const double t = W.cross(CA).dot(N) / N.dot(N);  // For B
  const double u = BA.cross(W).dot(N) / N.dot(N);  // For C
  const double s = 1 - t - u;                      // For A
  return (s >= 0.0) && (t >= 0.0) && (u >= 0.0);
}

const Vec3 TangentImages::TangentUVTo3D(const size_t tangent_image_idx,
                                        const double u, const double v) const {
  // Get the corners of this tangent image as 3D coordinates
  const size_t corner_idx = 4 * tangent_image_idx;  // Vector index
  Vec3 tl = this->corners[corner_idx];
  Vec3 tr = this->corners[corner_idx + 1];
  Vec3 bl = this->corners[corner_idx + 2];

  // Normalize the (u,v) coordinates on the tangent image
  const double un = u / this->dim;
  const double vn = v / this->dim;

  // Vector between TL and TR
  const Vec3 top = tr - tl;

  // Vector between TL and BL
  const Vec3 left = bl - tl;

  // Return converted UV coordinate in spherical coordinates
  return tl + un * top + vn * left;
}

const bool TangentImages::IsValidRegion(const size_t tangent_image_idx,
                                        const Vec3 &pt) const {
  // Check if the point falls within the valid region of the tangent image: the
  // icoshedron face, not the padding
  std::vector<double> vertices;
  this->GetIcosahedronVertices(vertices);
  const size_t vertices_idx = 9 * tangent_image_idx;
  const Vec3 A = Eigen::Map<Vec3>(vertices.data() + vertices_idx);
  const Vec3 B = Eigen::Map<Vec3>(vertices.data() + vertices_idx + 3);
  const Vec3 C = Eigen::Map<Vec3>(vertices.data() + vertices_idx + 6);

  // Return if the ray to the tangent image features in 3D intersects this
  // icosahedral face
  return this->RayIntersectsTriangle(pt, A, B, C);
}

const std::unique_ptr<features::Regions>
TangentImages::ConvertTangentImageFeaturesToEquirectangular(
    const std::vector<std::unique_ptr<features::Regions>> &regions) const {
  // Assert that the size of the vector being passed in matches the number of
  // tangent images
  if (regions.size() != this->num) {
    throw std::runtime_error(
        "The size of the regions vector must match the number of tangent "
        "images");
  }

  // Go through each set of tangent image features and convert them to their
  // equirectangular coordinates
#pragma omp parallel for
  for (size_t i = 0; i < regions.size(); i++) {
    const auto features = regions[i]->GetRegionsPositions();

    // Go through each point feature
    for (const auto &feat : features) {
      // Check that the coordinates fall within
      const double u = static_cast<double>(feat.x());
      const double v = static_cast<double>(feat.y());

      // Convert the feature location from UV on the tangent image to XYZ in 3D
      const Vec3 pt = this->TangentUVTo3D(i, u, v);

      // Check if this point falls within the valid region of the tangent image
      // or if it is in the padding
      if (this->IsValidRegion(i, pt)) {
        // If it is valid, convert 3D to spherical and then spherical to XY on
        // the equirectangular image
        const Vec2 sph_pt = Convert3DToSpherical(pt);
        const int x = Rescale(sph_pt[0], -M_PI, M_PI, 0.0,
                              static_cast<double>(this->rect_w - 1));
        const int y = Rescale(sph_pt[1], -M_PI / 2.0, M_PI / 2.0, 0.0,
                              static_cast<double>(this->rect_h - 1));

        // TOOD: Last step is to update the coordinates of the feature and add
        // it to a new Regions object that represents the equirectangular image
      }
    }
  }
  // return ...
}

// Average angle between vertices in a <base_level> icosahedron (radians)
const double TangentImages::GetVertexAngularResolution() const {
  switch (this->base_level) {
    case 0:
      return kVertexAngularResolutionL0;
    case 1:
    default:
      return kVertexAngularResolutionL1;
    case 2:
      return kVertexAngularResolutionL2;
  }
}

// Assigns the provides vector to the vector representing the N x 2 matrix of
// tangent image center (i.e. tangent points to the sphere) in spherical
// coordinates (lon, lat)
void TangentImages::GetTangentImageCenters(std::vector<double> &centers) const {
  switch (this->base_level) {
    case 0:
      centers = kTangentImageCentersL0;
      break;
    case 1:
    default:
      centers = kTangentImageCentersL1;
      break;
    case 2:
      centers = kTangentImageCentersL2;
      break;
  }
}

// Assigns the provides vector to the vector representing the N x 3 x 3
// (XYZ)tensor of tangent image center (i.e. tangent points to the sphere) in
// spherical coordinates (lon, lat)
void TangentImages::GetIcosahedronVertices(
    std::vector<double> &triangles) const {
  switch (this->base_level) {
    case 0:
      triangles = kIcosahedronVerticesL0;
      break;
    case 1:
    default:
      triangles = kIcosahedronVerticesL1;
      break;
    case 2:
      triangles = kIcosahedronVerticesL2;
      break;
  }
}

